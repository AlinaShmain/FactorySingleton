package com.company;

public class ContactInfo {
    String email;
    String telNumber;

    void setEmail(String email){
        this.email = email;
    }

    void setTelNumber(String telNumber){
        this.telNumber = telNumber;
    }

    String getEmail(){
        return email;
    }

    String getTelNumber(){
        return telNumber;
    }
}
